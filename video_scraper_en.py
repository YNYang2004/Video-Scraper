def convert_json_to_netscape(json_path, output_path):
    """Convert JSON cookies to Netscape 7 format"""
    with open(json_path, 'r', encoding='utf-8') as f:
        cookies = json.load(f)
    
    with open(output_path, 'w', encoding='utf-8', newline='') as f:
        f.write("# Netscape HTTP Cookie File\n")
        f.write("# This file is generated by Video Scraper to fix JSON error\n")
        for c in cookies:
            domain = c.get('domain', '')
            include_sub = "TRUE" if domain.startswith('.') else "FALSE"
            path = c.get('path', '/')
            secure = "TRUE" if c.get('secure') else "FALSE"
            try:
                expiry = int(float(c.get('expirationDate', time.time() + 31536000)))
            except:
                expiry = int(time.time() + 31536000)
            name = str(c.get('name', ''))
            value = str(c.get('value', ''))
            f.write(f"{domain}\t{include_sub}\t{path}\t{secure}\t{expiry}\t{name}\t{value}\n")

def prepare_cookie(cookie_src, log_signal=None):
    """Pre-process cookie paths"""
    if not cookie_src: return None
    
    final_cookie_path = None
    if os.path.exists(cookie_src):
        if cookie_src.lower().endswith('.json'):
            try:
                final_cookie_path = os.path.abspath("cookies_converted.txt")
                convert_json_to_netscape(cookie_src, final_cookie_path)
                if log_signal: log_signal.emit(f"[*] Converted {os.path.basename(cookie_src)} to Netscape format")
            except Exception as e:
                if log_signal: log_signal.emit(f"[!] Cookie conversion failed: {str(e)}")
                return cookie_src # Fallback to original
        else:
            final_cookie_path = os.path.abspath(cookie_src)
    return final_cookie_path

