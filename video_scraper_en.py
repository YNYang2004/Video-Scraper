class InfoThread(QThread):
    """Video info parsing thread"""
    info_signal = pyqtSignal(dict, bytes) # metadata, thumbnail_bytes
    error_signal = pyqtSignal(str)
    log_signal = pyqtSignal(str)

    def __init__(self, url, cookie_file, proxy=None):
        super().__init__()
        self.url = url
        self.cookie_file = cookie_file
        self.proxy = proxy

    def run(self):
        cookie_path = prepare_cookie(self.cookie_file, self.log_signal)
        
        # Bilibili parsing logic
        if "bilibili.com" in self.url or "b23.tv" in self.url:
            bv_match = re.search(r"(BV[a-zA-Z0-9]+)", self.url)
            if bv_match:
                bvid = bv_match.group(1)
                api_url = f"https://api.bilibili.com/x/web-interface/view?bvid={bvid}"
                headers = {"User-Agent": "Mozilla/5.0", "Referer": "https://www.bilibili.com/"}
                try:
                    res = requests.get(api_url, headers=headers, timeout=10).json()
                    if res.get('code') == 0:
                        data = res['data']
                        entries = []
                        # Handle UGC Seasons
                        if data.get('ugc_season'):
                            for section in data['ugc_season'].get('sections', []):
                                for ep in section.get('episodes', []):
                                    entries.append({'title': ep['title'], 'url': f"https://www.bilibili.com/video/{ep['bvid']}"})
                        # Handle Multi-P
                        elif len(data.get('pages', [])) > 1:
                            for p in data['pages']:
                                entries.append({'title': p['part'], 'url': f"https://www.bilibili.com/video/{bvid}?p={p['page']}"})
                        
                        if entries:
                            info = {
                                'title': data['title'],
                                'thumbnail': data['pic'],
                                'entries': entries,
                                'is_collection': True
                            }
                            self._emit_with_thumb(info)
                            return
                except Exception as e:
                    self.log_signal.emit(f"[*] Bilibili API skip: {e}")

        # YouTube/General logic
        ydl_opts = {
            'extract_flat': True,
            'cookiefile': cookie_path,
            'proxy': self.proxy,
            'nocheckcertificate': True,
            'quiet': True,
        }

        try:
            self.log_signal.emit(f"[*] Analyzing via engine: {self.url}")
            with yt_dlp.YoutubeDL(ydl_opts) as ydl:
                info = ydl.extract_info(self.url, download=False)
                
                if 'entries' in info:
                    processed_entries = []
                    for entry in info['entries']:
                        if not entry: continue
                        v_id = entry.get('id') or entry.get('url', '')
                        v_url = v_id if v_id.startswith('http') else f"https://www.youtube.com/watch?v={v_id}"
                        processed_entries.append({'title': entry.get('title', 'Unknown'), 'url': v_url})
                    info['entries'] = processed_entries

            self._emit_with_thumb(info)
        except Exception as e:
            self.error_signal.emit(str(e))

    def _emit_with_thumb(self, info):
        thumb_url = info.get('thumbnail')
        thumb_data = b""
        if thumb_url:
            try:
                req = urllib.request.Request(thumb_url, headers={'User-Agent': 'Mozilla/5.0'})
                with urllib.request.urlopen(req, timeout=5) as response:
                    thumb_data = response.read()
            except: pass
        self.info_signal.emit(info, thumb_data)

class DownloadThread(QThread):
    """Background download thread"""
    progress_signal = pyqtSignal(dict)
    finished_signal = pyqtSignal(str)
    error_signal = pyqtSignal(str)
    log_signal = pyqtSignal(str)

    def __init__(self, urls, save_path, cookie_file, proxy=None):
        super().__init__()
        self.urls = urls 
        self.save_path = save_path
        self.cookie_file = cookie_file
        self.proxy = proxy

    def run(self):
        cookie_path = prepare_cookie(self.cookie_file, self.log_signal)
        
        ydl_opts = {
            'format': 'bestvideo+bestaudio/best',
            'outtmpl': os.path.join(self.save_path, '%(title)s.%(ext)s'),
            'merge_output_format': 'mp4',
            'progress_hooks': [self._hook],
            'cookiefile': cookie_path,
            'proxy': self.proxy,
            'nocheckcertificate': True,
            'no_warnings': False,
        }

        try:
            with yt_dlp.YoutubeDL(ydl_opts) as ydl:
                self.log_signal.emit(f"[*] Starting download for {len(self.urls)} tasks...")
                ydl.download(self.urls)
            self.finished_signal.emit("All tasks completed!")
        except Exception as e:
            self.error_signal.emit(str(e))

    def _hook(self, d):
        if d['status'] == 'downloading':
            self.progress_signal.emit(d)
        elif d['status'] == 'finished':
            self.log_signal.emit(f"[Done] File: {os.path.basename(d.get('filename', 'Unknown'))}")

