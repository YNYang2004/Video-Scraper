    def browse_cookie(self):
        file, _ = QFileDialog.getOpenFileName(self, "Select Cookie File", "", "Cookies (*.json *.txt)")
        if file: 
            self.cookie_input.setText(file)
            self.settings.setValue("cookie_path", file)

    def load_saved_settings(self):
        saved_cookie = self.settings.value("cookie_path", "")
        if saved_cookie and os.path.exists(saved_cookie):
            self.cookie_input.setText(saved_cookie)
            self.log_output.append(f"[*] Auto-loaded cookie: {os.path.basename(saved_cookie)}")
        
        proxy_enabled = self.settings.value("proxy_enabled", "false") == "true"
        proxy_address = self.settings.value("proxy_address", "")
        self.proxy_check.setChecked(proxy_enabled)
        self.proxy_input.setText(proxy_address)

    def browse_path(self):
        directory = QFileDialog.getExistingDirectory(self, "Select Download Directory")
        if directory: self.path_input.setText(directory)

    def analyze_url(self):
        url = self.url_input.text().strip()
        if not url:
            self.log_output.append("[!] Please enter a video URL first")
            return
        
        proxy = self.proxy_input.text().strip() if self.proxy_check.isChecked() else None
        
        self.analyze_btn.setEnabled(False)
        self.log_output.append(f"[*] Analyzing URL: {url}...")
        
        self.info_thread = InfoThread(url, self.cookie_input.text(), proxy)
        self.info_thread.info_signal.connect(self.on_info_loaded)
        self.info_thread.log_signal.connect(lambda m: self.log_output.append(m))
        self.info_thread.error_signal.connect(self.on_error)
        self.info_thread.finished.connect(lambda: self.analyze_btn.setEnabled(True))
        self.info_thread.start()

    def on_info_loaded(self, info, thumb_data):
        self.preview_group.setVisible(True)
        self.playlist_widget.clear()
        
        title = info.get('title', 'Unknown Title')
        self.title_label.setText(f"Title: {title}")
        
        if thumb_data:
            pixmap = QPixmap()
            pixmap.loadFromData(thumb_data)
            self.thumb_label.setPixmap(pixmap)
        
        if 'entries' in info and len(info['entries']) > 0:
            self.playlist_widget.setVisible(True)
            self.list_controls.setVisible(True)
            self.log_output.append(f"[*] Detected list/collection with {len(info['entries'])} videos")
            
            for i, entry in enumerate(info['entries'], 1):
                item_title = entry.get('title', 'Unknown')
                item_url = entry.get('url') or entry.get('webpage_url')
                display_text = f"[{i:02d}] {item_title}"
                
                item = QListWidgetItem(display_text)
                item.setData(Qt.ItemDataRole.UserRole, item_url)
                item.setFlags(item.flags() | Qt.ItemFlag.ItemIsUserCheckable | Qt.ItemFlag.ItemIsEnabled | Qt.ItemFlag.ItemIsSelectable)
                item.setCheckState(Qt.CheckState.Checked) 
                self.playlist_widget.addItem(item)
            self.current_targets = [] 
        else:
            self.playlist_widget.setVisible(False)
            self.current_targets = [info.get('webpage_url', self.url_input.text())]
            self.log_output.append("[*] Single video analysis complete")

        self.download_btn.setEnabled(True)
        self.download_btn.setText("Download Selected" if self.playlist_widget.isVisible() else "Start Download")

    def start_download(self):
        targets = []
        if self.playlist_widget.isVisible():
            for i in range(self.playlist_widget.count()):
                item = self.playlist_widget.item(i)
                if item.checkState() == Qt.CheckState.Checked:
                    targets.append(item.data(Qt.ItemDataRole.UserRole))
        else:
            targets = self.current_targets

        if not targets:
            QMessageBox.warning(self, "Warning", "Please select at least one video to download")
            return

        self.download_btn.setEnabled(False)
        self.progress_bar.setValue(0)
        save_path = self.path_input.text()
        if not os.path.exists(save_path): os.makedirs(save_path)

        proxy = self.proxy_input.text().strip() if self.proxy_check.isChecked() else None

        self.thread = DownloadThread(targets, save_path, self.cookie_input.text(), proxy)
        self.thread.progress_signal.connect(self.update_progress)
        self.thread.log_signal.connect(lambda m: self.log_output.append(m))
        self.thread.finished_signal.connect(self.on_finished)
        self.thread.error_signal.connect(self.on_error)
        self.thread.start()

    def update_progress(self, d):
        p_str = d.get('_percent_str', '0%').replace('%', '')
        try:
            val = int(float(p_str))
            self.progress_bar.setValue(val)
        except: pass
        
        speed = d.get('_speed_str', 'N/A')
        eta = d.get('_eta_str', 'N/A')
        self.statusBar().showMessage(f"Speed: {speed} | ETA: {eta}")

    def on_finished(self, msg):
        self.log_output.append(f"<b>[Success] {msg}</b>")
        self.download_btn.setEnabled(True)
        self.statusBar().showMessage("Ready")

    def select_all_items(self):
        for i in range(self.playlist_widget.count()):
            self.playlist_widget.item(i).setCheckState(Qt.CheckState.Checked)

    def select_none_items(self):
        for i in range(self.playlist_widget.count()):
            self.playlist_widget.item(i).setCheckState(Qt.CheckState.Unchecked)

    def playlist_key_press(self, event):
        if event.key() == Qt.Key.Key_Space:
            selected_items = self.playlist_widget.selectedItems()
            if not selected_items: return
            
            target_state = Qt.CheckState.Unchecked if selected_items[0].checkState() == Qt.CheckState.Checked else Qt.CheckState.Checked
            self.playlist_widget.blockSignals(True)
            for item in selected_items:
                item.setCheckState(target_state)
            self.playlist_widget.blockSignals(False)
        else:
            QListWidget.keyPressEvent(self.playlist_widget, event)

    def sync_checkbox_state(self, clicked_item):
        selected_items = self.playlist_widget.selectedItems()
        if clicked_item in selected_items:
            new_state = clicked_item.checkState()
            self.playlist_widget.blockSignals(True)
            for item in selected_items:
                item.setCheckState(new_state)
            self.playlist_widget.blockSignals(False)

    def on_error(self, err):
        self.log_output.append(f"<font color='red'>[Error] {err}</font>")
        self.download_btn.setEnabled(True)
        self.analyze_btn.setEnabled(True)
        
        if "Netscape formatted" in err or "cookies" in err.lower():
            self.show_cookie_help()

    def show_cookie_help(self):
        msg = QMessageBox(self)
        msg.setWindowTitle("Cookie Format Guide")
        msg.setIcon(QMessageBox.Icon.Warning)
        msg.setText("Detected cookie file issues. Recommendations:")
        msg.setInformativeText(
            "1. Install <b>Cookie-Editor</b> extension in your browser<br>"
            "2. Open the video site and log in<br>"
            "3. Click extension -> Export -> <b>Netscape HTTP Cookie File</b><br>"
            "4. Save content as cookies.txt and reload in this app"
        )
        msg.exec()

